; -----------------------------------------------------------------------------
; PINOUT
; 1. VCC
; 2. PB1 = USR
; 3. PB0 = PWR
; 4. PA3 = unused
; 
; 5. PA2 = LED
; 6. PA1 = PWR_CTRL_1 (LED2)
; 7. PA0 = PWR_CTRL_2 (LED3)
; 8. GND
; -----------------------------------------------------------------------------


; -----------------------------------------------------------------------------
; Include Block
; -----------------------------------------------------------------------------

#include     "power.inc"

; -----------------------------------------------------------------------------
; PROGRAM START
; -----------------------------------------------------------------------------
	ORG	0
	LDPCH	PGMSRT
	JMP	PGMSRT

	NOP
	NOP
	
	LDPCH	WAKEUP
	JMP	WAKEUP

	NOP
	NOP
	
;-------------------
;MAIN PROGRAM
;-------------------
PGMSRT:


WAKEUP:

	
MAIN_LOOP:





	LDPCH	MAIN_LOOP
	JMP	MAIN_LOOP

;----------------------
;Link Data
;----------------------





